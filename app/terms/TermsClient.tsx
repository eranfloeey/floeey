"use client";

import { useEffect, useState } from "react";
import { useRouter, useSearchParams } from "next/navigation";

type TabId = "terms" | "privacy" | "cancellation";

const TABS: { id: TabId; label: string }[] = [
  { id: "terms", label: "תקנון ותנאי שימוש" },
  { id: "privacy", label: "מדיניות פרטיות" },
  { id: "cancellation", label: "מדיניות ביטולים" },
];

const COMPANY = 'פלואיי בע"מ';
const EMAIL = "support@floeey.com";

export default function TermsClient() {
  const router = useRouter();
  const sp = useSearchParams();
  const initial = (sp.get("tab") as TabId) || "terms";
  const [tab, setTab] = useState<TabId>(initial);

  useEffect(() => {
    const t = (sp.get("tab") as TabId) || "terms";
    if (t !== tab) setTab(t);
    // eslint-disable-next-line react-hooks/exhaustive-deps
  }, [sp]);

  function selectTab(id: TabId) {
    setTab(id);
    router.push(`/terms?tab=${id}`);
  }

  return (
    <section className="terms-page" dir="rtl">
      <div className="terms-glow" aria-hidden="true" />
      <div className="terms-container">
        <h1 className="terms-h1">תנאי שימוש</h1>

        <div className="terms-tabs" role="tablist">
          {TABS.map((t) => (
            <button
              key={t.id}
              role="tab"
              aria-selected={tab === t.id}
              onClick={() => selectTab(t.id)}
              className={`terms-tab ${tab === t.id ? "is-active" : ""}`}
            >
              {t.label}
            </button>
          ))}
        </div>

        <div className="terms-card">
          {tab === "terms" && <TermsContent />}
          {tab === "privacy" && <PrivacyContent />}
          {tab === "cancellation" && <CancellationContent />}
        </div>
      </div>
    </section>
  );
}

